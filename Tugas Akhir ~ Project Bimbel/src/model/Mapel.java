package model;

public class Mapel {
    private String namaMapel , kodeMapel;
    public Mapel(String namaMapel , String kodeMapel){
        this.namaMapel = namaMapel;
        this.kodeMapel = kodeMapel;
    }

    public String getNamaMapel() {
        return namaMapel;
    }

    public String getKodeMapel() {
        return kodeMapel;
    }
}
